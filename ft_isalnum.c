#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	ch;

	ch = c;
	return ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z')
		|| (ch >= '0' && ch <= '9'));
}
