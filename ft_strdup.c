#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		len;
	int		i;
	int		j;

	len = ft_strlen(s);
	dup = malloc(sizeof(char) * (len + 1));
	if (!dup)
		return (NULL);
	i = 0;
	j = 0;
	while (s[i])
		dup[j++] = s[i++];
	dup[j] = '\0';
	return (dup);
}
